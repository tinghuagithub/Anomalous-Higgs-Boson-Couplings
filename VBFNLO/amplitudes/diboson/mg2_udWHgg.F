      REAL*8 FUNCTION UD_WHGG(PBAR,NHEL,fsign)
C  
C Generated by MadGraph II                                              
C RETURNS AMPLITUDE SQUARED SUMMED/AVG OVER COLORS
C FOR THE POINT WITH EXTERNAL LINES W(0:6,NEXTERNAL)
C  
C FOR PROCESS : u d~ -> ve e+ h g g  
C  
      IMPLICIT NONE
C  
C CONSTANTS
C  
      INTEGER    NGRAPHS,    NEIGEN 
      PARAMETER (NGRAPHS=   8,NEIGEN=  2) 
#include "VBFNLO/utilities/global.inc"
#include "tensor.inc"

      integer    nexternal
      parameter (nexternal=  8)
      integer    nincoming
      parameter (nincoming=  2)
      integer    maxamps, maxflow
      parameter (maxamps=    8,maxflow=   2)
      INTEGER    NWAVEFUNCS     , NCOLOR
      PARAMETER (NWAVEFUNCS=  19, NCOLOR=   2) 
      REAL*8     ZERO
      PARAMETER (ZERO=0D0)
C  
C ARGUMENTS 
C  
      REAL*8 PBAR(0:3,NEXTERNAL)
      INTEGER NHEL(NEXTERNAL), fsign(NEXTERNAL)
C  
C LOCAL VARIABLES 
C  
      INTEGER I,J
      COMPLEX*16 ZTEMP
      REAL*8 DENOM(NCOLOR), CF(NCOLOR,NCOLOR)
      COMPLEX*16 AMP(NGRAPHS), JAMP(NCOLOR)
      COMPLEX*16 W(18,NWAVEFUNCS)

      DOUBLE COMPLEX subprocess(3)
C  
C GLOBAL VARIABLES
C  
      Double Precision amp2(maxamps), jamp2(0:maxflow)
      common/to_ampsdi660_3/  amp2,       jamp2
      include "VBFNLO/utilities/coupl.inc"
C  
C COLOR DATA
C  
      DATA Denom(1  )/            3/                                       
      DATA (CF(i,1  ),i=1  ,2  ) /    16,   -2/                            
C               T[ 2, 1, 6, 7]                                             
      DATA Denom(2  )/            3/                                       
      DATA (CF(i,2  ),i=1  ,2  ) /    -2,   16/                            
C               T[ 2, 1, 7, 6]                                             
C ----------
C BEGIN CODE
C ----------
      CALL IXXXXX(PBAR(0,1   ),ZERO ,NHEL(1   )*FSIGN(1),FSIGN(1   ),W(1,1   ))        
      CALL OXXXXX(PBAR(0,2   ),ZERO ,NHEL(2   )*FSIGN(2),FSIGN(2   ),W(1,2   ))        
      CALL OXXXXX(PBAR(0,3   ),ZERO ,NHEL(3   )*FSIGN(3),FSIGN(3   ),W(1,3   ))        
      CALL IXXXXX(PBAR(0,4   ),ZERO ,NHEL(4   )*FSIGN(4),FSIGN(4   ),W(1,4   ))        
      CALL SXXXXX(PBAR(0,5   ),FSIGN(5   )*FSIGN(5),W(1,5   ))                         
      CALL VXXXXX(PBAR(0,7   ),ZERO ,NHEL(6   )*FSIGN(7),FSIGN(7   ),W(1,6   ))        
      CALL VXXXXX(PBAR(0,8   ),ZERO ,NHEL(7   )*FSIGN(8),FSIGN(8   ),W(1,7   ))        
      CALL JIOXXX(W(1,4   ),W(1,3   ),GWF ,WMASS   ,WWIDTH  ,W(1,8   ))    
      CALL JVSXXX(W(1,8   ),W(1,5   ),GWWH ,WMASS   ,WWIDTH  ,W(1,         
     &     9   ))                                                          
      CALL FVIXXX(W(1,1   ),W(1,7   ),GG ,ZERO    ,ZERO    ,W(1,10  ))     
      CALL FVOXXX(W(1,2   ),W(1,9   ),GWF ,ZERO    ,ZERO    ,W(1,11  ))    
      CALL IOVXXX(W(1,10  ),W(1,11  ),W(1,6   ),GG ,AMP(1   ))             
      CALL FVOXXX(W(1,2   ),W(1,7   ),GG ,ZERO    ,ZERO    ,W(1,12  ))     
      CALL FVOXXX(W(1,12  ),W(1,9   ),GWF ,ZERO    ,ZERO    ,W(1,13  ))    
      CALL IOVXXX(W(1,1   ),W(1,13  ),W(1,6   ),GG ,AMP(2   ))             
      CALL JIOXXX(W(1,1   ),W(1,11  ),GG ,ZERO    ,ZERO    ,W(1,14  ))     
      CALL VVVXXX(W(1,7   ),W(1,6   ),W(1,14  ),G ,AMP(3   ))              
      CALL FVOXXX(W(1,11  ),W(1,7   ),GG ,ZERO    ,ZERO    ,W(1,15  ))     
      CALL IOVXXX(W(1,1   ),W(1,15  ),W(1,6   ),GG ,AMP(4   ))             
      CALL FVIXXX(W(1,10  ),W(1,9   ),GWF ,ZERO    ,ZERO    ,W(1,16  ))    
      CALL IOVXXX(W(1,16  ),W(1,2   ),W(1,6   ),GG ,AMP(5   ))             
      CALL FVIXXX(W(1,1   ),W(1,9   ),GWF ,ZERO    ,ZERO    ,W(1,17  ))    
      CALL IOVXXX(W(1,17  ),W(1,12  ),W(1,6   ),GG ,AMP(6   ))             
      CALL JIOXXX(W(1,17  ),W(1,2   ),GG ,ZERO    ,ZERO    ,W(1,18  ))     
      CALL VVVXXX(W(1,7   ),W(1,6   ),W(1,18  ),G ,AMP(7   ))              
      CALL FVIXXX(W(1,17  ),W(1,7   ),GG ,ZERO    ,ZERO    ,W(1,19  ))     
      CALL IOVXXX(W(1,19  ),W(1,2   ),W(1,6   ),GG ,AMP(8   ))             

      subprocess(1) = -amp(1)-amp(5)-amp(8)
      subprocess(2) = -amp(2)-amp(4)-amp(6)
      subprocess(3) = AMP(3) + AMP(7)

c--------------------------------------------------------------------

      JAMP(1) =   subprocess(1)+subprocess(3)
      JAMP(2) =   subprocess(2)-subprocess(3)

c------------------------------------------------------------------
c comparison with hand made code
      do i = 1,3
         if ((nhel(1).eq.nhel(2)).and.
     2        (nhel(3).eq.-1).and.(nhel(4).eq.-1)) then
!            print*, i, nhel(1), nhel(6), nhel(7), 1
            tamprr(i,nhel(1),nhel(6),nhel(7),1) 
     &            = subprocess(i)/g**2 ! gluon pol. in helicity basis
           endif
      enddo ! divide out strong coupling
c------------------------------------------------------------------

      UD_WHGG = 0.D0 
      DO I = 1, NCOLOR
          ZTEMP = (0.D0,0.D0)
          DO J = 1, NCOLOR
              ZTEMP = ZTEMP + CF(J,I)*JAMP(J)
          ENDDO
          UD_WHGG =UD_WHGG+ZTEMP*DCONJG(JAMP(I))/DENOM(I)   
      ENDDO
      Do I = 1, NGRAPHS
          amp2(i)=amp2(i)+amp(i)*dconjg(amp(i))
      Enddo
      Do I = 1, NCOLOR
          Jamp2(i)=Jamp2(i)+Jamp(i)*dconjg(Jamp(i))
      Enddo
C      CALL GAUGECHECK(JAMP,ZTEMP,EIGEN_VEC,EIGEN_VAL,NCOLOR,NEIGEN) 
      END
       
       
