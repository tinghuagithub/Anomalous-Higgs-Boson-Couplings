        subroutine FermionBox_Div(m,p1,p2,p3,p4,mup1,mup2,mup3,mup4,mus
     -   q,gauge,comp,result,resultgauge,Div)
       implicit none
       Real*8   p1(0:3),p2(0:3),p3(0:3),p4(0:3)
       Real*8   p12(0:3),p14(0:3),p23(0:3),p34(0:3)
       Complex*16 mup1(0:3),mup2(0:3),mup3(0:3),mup4(0:3)
       Complex*16 mup1test(0:3),mup2test(0:3),mup3test(0:3),mup4test(0:3)
       Complex*16 result,resulttemp,resulttemp2
       Real*8 musq,m,resultgauge,temp1,temp2,temp3,temp4
       complex*16 rg1,rg2,rg3,rg4
       integer gauge,i,comp,Div

        resultgauge=1d99

       call  FermionBoxEven_Div(m,p1,p2,p3,p4,mup1,mup2,mup3,mup4
     &   ,musq,comp,result,Div)

       if (gauge.gt.0) then
       
         do  i=0,3
            mup1test(i)=p1(i)
            mup2test(i)=p2(i)
            mup3test(i)=p3(i)
            mup4test(i)=p4(i)
            p12(i)= p1(i)+p2(i)
            p14(i)= p1(i)+p4(i)
            p23(i)= p2(i)+p3(i)
            p34(i)= p3(i)+p4(i)
         enddo
       
c Call for Gauge

          call fermionBoxEven_Div(m,p1,p2,p3,p4,mup1test,mup2,mup3,mup4,musq,-1,rg1,Div)
          call fermionBoxEven_Div(m,p1,p2,p3,p4,mup1,mup2test,mup3,mup4,musq,-1,rg2,Div)
          call fermionBoxEven_Div(m,p1,p2,p3,p4,mup1,mup2,mup3test,mup4,musq,-1,rg3,Div)
          call fermionBoxEven_Div(m,p1,p2,p3,p4,mup1,mup2,mup3,mup4test,musq,-1,rg4,Div)
c Gauge 1
          call fermionVertEven_Div(m,p12,p3,p4,mup2,mup3,mup4,musq,resulttemp ,Div) 
          call fermionVertEven_Div(m,p2,p3,p14,mup2,mup3,mup4,musq,resulttemp2,Div) 
          if (abs(rg1).gt.1d-7) then
          temp1=abs(rg1/(-resulttemp+resulttemp2) -1d0)
          else
          temp1=abs(rg1-(-resulttemp+resulttemp2))   
          endif

!         if (temp1.gt.1d-1) then
!             print*, "temp1", temp1,rg1,resulttemp,resulttemp2
!             read(*,*)
!          endif

c Gauge 2
          call fermionVertEven_Div(m,p1,p23,p4,mup1,mup3,mup4,musq,resulttemp ,Div)  
          call fermionVertEven_Div(m,p12,p3,p4,mup1,mup3,mup4,musq,resulttemp2,Div) 
         if (abs(rg2).gt.1d-7) then
          temp2=abs(rg2/(-resulttemp+resulttemp2) -1d0) 
          else
          temp2=abs(rg2 -(-resulttemp+resulttemp2) ) 
          endif

!         if (temp1.gt.1d-1) then
!             print*, "temp2", temp2,rg2,resulttemp,resulttemp2
!             read(*,*)
!          endif
c Gauge 3
          call fermionVertEven_Div(m,p1,p2,p34,mup1,mup2,mup4,musq,resulttemp ,Div) 
          call fermionVertEven_Div(m,p1,p23,p4,mup1,mup2,mup4,musq,resulttemp2,Div) 
         if (abs(rg3).gt.1d-7) then
          temp3=abs(rg3/(-resulttemp+resulttemp2) -1d0 )
          else
             temp3=abs(rg3-(-resulttemp+resulttemp2) )
         endif

c Gauge 4
          call fermionVertEven_Div(m,p2,p3,p14,mup2,mup3,mup1,musq,resulttemp ,Div) 
          call fermionVertEven_Div(m,p1,p2,p34,mup1,mup2,mup3,musq,resulttemp2,Div)
          if (abs(rg4).gt.1d-7) then
          temp4=abs(rg4/(-resulttemp+resulttemp2) -1d0)
          else
          temp4=abs(rg4-(-resulttemp+resulttemp2))
          endif
      
c	read(*,*)

          resultgauge=max(temp1,temp2,temp3,temp4)
        
          if (resultgauge.gt.1d-1) then
             print*, "DIV", temp1,temp2,temp3,temp4
             read(*,*)
          endif

           endif
         
        end


        subroutine FermionBoxEven_Div(m,p1,p2,p3,p4,mup1,mup2,mup3,mup4,mus
     -   q,comp,result,Div)
c ************************************************************************************
c determine the fermion looop
c Note: No couplings are assigned
c Note: To make it shorter in the promgram: mu_p2,...->mup2,... 
c Notation of External momenta: p1+p2+p3+p4=0 
c mu_p1,mu_p2,mu_p3,mu_p4 should be think as external current 
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c    Declaration of variables 
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
       IMPLICIT NONE
       Real*8   p1(0:3),p2(0:3),p3(0:3),p4(0:3)
       Real*8   p1sq, p1p2, p1p3, p1p4, p2sq, p2p3, p2p4, p3sq, 
     -          p3p4, p4sq
       Real*8   s12, s13, s14, s23, s24, s34
       Complex*16   p1mup1, p1mup2, p1mup3, p1mup4, p2mup1, p2mup2, 
     -          p2mup3, p2mup4, p3mup1, p3mup2, p3mup3, p3mup4, 
     -          p4mup1, p4mup2, p4mup3, p4mup4
       Complex*16   mup1mup2, mup1mup3, mup1mup4, mup2mup3, mup2mup4, 
     -          mup3mup4
       Real*8 dotrr
       Complex*16 B0finDiv,C0finDiv,D0finDiv
       Complex*16 A0finDiv,B0tMDiv
       EXTERNAL dotrr,A0finDiv,B0tMDiv,B0finDiv,C0finDiv,D0finDiv
       Complex*16 A0,B012,B013,B014,B023,B024,B034
      Real*8 A0R,A0I,B012R,B013R,B014R,B023R,B024R,B034R
      Real*8 B012I,B013I,B014I,B023I,B024I,B034I
      Real*8 B12R(2,2),B13R(2,2),B14R(2,2),B23R(2,2),B24R(2,2),B34R(2,2)
      Real*8 B12I(2,2),B13I(2,2),B14I(2,2),B23I(2,2),B24I(2,2),B34I(2,2)
       Complex*16 C0123,C0124,C0134,C0234
       Real*8 C0123R,C0124R,C0134R,C0234R
       Real*8 C0123I,C0124I,C0134I,C0234I
       Real*8 C123R(6,3),C124R(6,3),C134R(6,3),C234R(6,3)
       Real*8 C123I(6,3),C124I(6,3),C134I(6,3),C234I(6,3)
       Complex*16 D01234
        Real*8 D01234R
       Real*8 D01234I
       Real*8 D1234R(22,4)
        Real*8 D1234I(22,4)
      Complex*16 F(135)
      Real*8 FI(135),FR(135),P(52)
      Complex*16 mup1(0:3),mup2(0:3),mup3(0:3),mup4(0:3)
      Complex*16 dotrc,dotcc,result
       Real*8 musq,m,msq
      EXTERNAL   dotrc,dotcc
      Integer comp,Div
      common/Ffermionbox/F
      common/Pfermionbox/P
      Save/Ffermionbox/
      Save/Pfermionbox/
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c       Definition of the scalar products. Not inlcueded the contraction of the
c       moments with the external currents  
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
       msq=m*m
       p1sq = dotrr(p1,p1)
       p1p2 = dotrr(p1,p2)
       p1p3 = dotrr(p1,p3)
       p1p4 = dotrr(p1,p4)
       p2sq = dotrr(p2,p2)
       p2p3 = dotrr(p2,p3)
       p2p4 = dotrr(p2,p4)
       p3sq = dotrr(p3,p3)
       p3p4 = dotrr(p3,p4)
       p4sq = dotrr(p4,p4)
       s12 = (p1sq +p2sq+ 2*p1p2) 
       s13 = (p1sq +p3sq+ 2*p1p3) 
       s14 = (p1sq +p4sq+ 2*p1p4) 
       s23 = (p2sq +p3sq+ 2*p2p3) 
       s24 = (p2sq +p4sq+ 2*p2p4) 
       s34 = (p3sq +p4sq+ 2*p3p4) 
c       Write(*,'(a5,F20.10)')," p1sq ", p1sq 
c       Write(*,'(a5,F20.10)')," p1p2 ", p1p2
c       Write(*,'(a5,F20.10)')," p1p3 ", p1p3
c       Write(*,'(a5,F20.10)')," p1p4 ", p1p4
c       Write(*,'(a5,F20.10)')," p2sq ", p2sq 
c       Write(*,'(a5,F20.10)')," p2p3 ", p2p3
c       Write(*,'(a5,F20.10)')," p2p4 ", p2p4
c       Write(*,'(a5,F20.10)')," p3sq ", p3sq 
c       Write(*,'(a5,F20.10)')," p3p4 ", p3p4
c       Write(*,'(a5,F20.10)')," p4sq ", p4sq 
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************

       if(comp.ge.1) then


        if(m.gt.1d-6) then
c    Calling C_ij,D_ij Functions    
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
        A0=A0finDiv(m,Div)  
        A0R=Dble(A0)   
        A0I=DImag(A0)   
       B012=B0tMDiv(m,p1sq,Div)  
       B013=B0tMDiv(m,s12,Div)   
       B014=B0tMDiv(m,p4sq,Div)   
       B023=B0tMDiv(m,p2sq,Div)  
       B024=B0tMDiv(m,s23,Div)   
       B034=B0tMDiv(m,p3sq,Div)  
c************************************************************************************
       call tens_red2_Re_1MDiv(m,p1sq,A0R,A0I,B012,B012R,B012I,B12R,B12I)       
       call tens_red2_Re_1MDiv(m,s12, A0R,A0I,B013,B013R,B013I,B13R,B13I) 
       call tens_red2_Re_1MDiv(m,p4sq,A0R,A0I,B014,B014R,B014I,B14R,B14I)
       call tens_red2_Re_1MDiv(m,p2sq,A0R,A0I,B023,B023R,B023I,B23R,B23I)
       call tens_red2_Re_1MDiv(m,s23, A0R,A0I,B024,B024R,B024I,B24R,B24I) 
       call tens_red2_Re_1MDiv(m,p3sq,A0R,A0I,B034,B034R,B034I,B34R,B34I)       
c************************************************************************************
      C0123=0d0
      C0124=0d0
      C0134=0d0
      C0234=0d0
c************************************************************************************
c************************************************************************************
c************************************************************************************
       call tens_red3_new_Re_Com_1MRank3Div(m,p1sq,p2sq,s12,B023R,B013R,B
     -   012R,B23R,B13R,B12R,B023I,B013I,B012I,B23I,B13I,B12I,C0123,C
     -   0123R,C0123I,C123R,C123I) 
       call tens_red3_new_Re_Com_1MRank3Div(m,p1sq,s23,p4sq,B024R,B014R,B
     -   012R,B24R,B14R,B12R,B024I,B014I,B012I,B24I,B14I,B12I,C0124,C
     -   0124R,C0124I,C124R,C124I) 
       call tens_red3_new_Re_Com_1MRank3Div(m,s12,p3sq,p4sq,B034R,B014R,B
     -   013R,B34R,B14R,B13R,B034I,B014I,B013I,B34I,B14I,B13I,C0134,C
     -   0134R,C0134I,C134R,C134I) 
       call tens_red3_new_Re_Com_1MRank3Div(m,p2sq,p3sq,s23,B034R,B024R,B
     -   023R,B34R,B24R,B23R,B034I,B024I,B023I,B34I,B24I,B23I,C0234,C
     -   0234R,C0234I,C234R,C234I) 
c************************************************************************************
c************************************************************************************
       D01234=0d0
c************************************************************************************
c************************************************************************************
       call tens_red4_new_Re_Com_1MRank4Div(m,p1sq,p2sq,p3sq,p1p2,p1p3,p2
     -   p3,C0234R,C0134R,C0124R,C0123R,C234R,C134R,C124R,C123R,C0234
     -   I,C0134I,C0124I,C0123I,C234I,C134I,C124I,C123I,D01234,D01234
     -   R,D01234I,D1234R,D1234I)
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
      elseif(m.lt.1d-6) then
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c    Calling C_ij,D_ij Functions    
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
       B012=B0finDiv(p1sq,musq,Div)   
       B013=B0finDiv(s12,musq,Div)   
       B014=B0finDiv(p4sq,musq,Div)   
       B023=B0finDiv(p2sq,musq,Div)  
       B024=B0finDiv(s23,musq,Div)   
       B034=B0finDiv(p3sq,musq,Div)  
       call tens_red2_ReDiv(p1sq,B012,B012R,B012I,B12R,B12I) 
       call tens_red2_ReDiv(s12, B013,B013R,B013I,B13R,B13I) 
       call tens_red2_ReDiv(p4sq,B014,B014R,B014I,B14R,B14I) 
       call tens_red2_ReDiv(p2sq,B023,B023R,B023I,B23R,B23I) 
       call tens_red2_ReDiv(s23, B024,B024R,B024I,B24R,B24I) 
       call tens_red2_ReDiv(p3sq,B034,B034R,B034I,B34R,B34I) 
c************************************************************************************
c************************************************************************************
c************************************************************************************
      C0123=C0finDiv(p1sq,p2sq,s12,musq,Div) 
      C0124=C0finDiv(p1sq,s23,p4sq,musq,Div)  
      C0134=C0finDiv(s12,p3sq,p4sq,musq,Div)  
      C0234=C0finDiv(p2sq,p3sq,s23,musq,Div) 
c************************************************************************************
c************************************************************************************
       call tens_red3_new_Re_ComRank3Div(p1sq,p2sq,s12,B023R,B013R,B012R,
     -   B23R,B13R,B12R,B023I,B013I,B012I,B23I,B13I,B12I,C0123,C0123R
     -   ,C0123I,C123R,C123I) 
       call tens_red3_new_Re_ComRank3Div(p1sq,s23,p4sq,B024R,B014R,B012R,
     -   B24R,B14R,B12R,B024I,B014I,B012I,B24I,B14I,B12I,C0124,C0124R
     -   ,C0124I,C124R,C124I) 
       call tens_red3_new_Re_ComRank3Div(s12,p3sq,p4sq,B034R,B014R,B013R,
     -   B34R,B14R,B13R,B034I,B014I,B013I,B34I,B14I,B13I,C0134,C0134R
     -   ,C0134I,C134R,C134I) 
       call tens_red3_new_Re_ComRank3Div(p2sq,p3sq,s23,B034R,B024R,B023R,
     -   B34R,B24R,B23R,B034I,B024I,B023I,B34I,B24I,B23I,C0234,C0234R
     -   ,C0234I,C234R,C234I) 
c************************************************************************************
c************************************************************************************
         D01234=D0finDiv(s12,s23,p1sq,p2sq,p3sq,p4sq,musq,Div)
c       Print*," D0",D0
c************************************************************************************
c************************************************************************************
       call tens_red4_new_Re_ComRank4Div(p1sq,p2sq,p3sq,p1p2,p1p3,p2p3,C0
     -   234R,C0134R,C0124R,C0123R,C234R,C134R,C124R,C123R,C0234I,C01
     -   34I,C0124I,C0123I,C234I,C134I,C124I,C123I,D01234,D01234R,D01
     -   234I,D1234R,D1234I)
         endif
c       Definition of the F,P functions:Independent of the currents    
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
       P(1) = p1sq*p3sq+p2sq*p4sq-s12*s23
       P(2) = p1sq+p2sq+p3sq+p4sq-2*s23
       P(3) = -p1sq-p2sq+s12
       P(4) = -p1sq-p4sq+s23
       P(5) = -p3sq-p4sq+s12
       P(6) = -p2sq-p3sq+s23
       FR(1) = 2*(B013R+B024R)-8*(C124R(4,2)+C234R(4,2))+32*D1234R(22,
     -   4)+D01234R*P(1)+4*D1234R(7,2)*P(2)+C0123R*P(3)+C0124R*P(4)+C
     -   0134R*P(5)+C0234R*P(6)
       FI(1) = 2*(B013I+B024I)-8*(C124I(4,2)+C234I(4,2))+32*D1234I(22,
     -   4)+D01234I*P(1)+4*D1234I(7,2)*P(2)+C0123I*P(3)+C0124I*P(4)+C
     -   0134I*P(5)+C0234I*P(6)
       F(1)=DCMPLX(FR(1),FI(1))
       P(7) = -(p1sq*p3sq)-p2sq*p4sq+s12*s23
       P(8) = s12+s23
       P(9) = p1sq+p2sq+p3sq+p4sq-2*P(8)
       P(10) = p1sq+p2sq-s12
       P(11) = p1sq+p4sq-s23
       P(12) = p3sq+p4sq-s12
       P(13) = p2sq+p3sq-s23
       FR(2) = -2*(B013R+B024R)+32*D1234R(22,4)+D01234R*P(7)-4*D1234R(
     -   7,2)*P(9)+C0123R*P(10)+C0124R*P(11)+C0134R*P(12)+C0234R*P(13
     -   )
       FI(2) = -2*(B013I+B024I)+32*D1234I(22,4)+D01234I*P(7)-4*D1234I(
     -   7,2)*P(9)+C0123I*P(10)+C0124I*P(11)+C0134I*P(12)+C0234I*P(13
     -   )
       F(2)=DCMPLX(FR(2),FI(2))
       P(14) = p1sq+p2sq+p3sq+p4sq-2*s12
       FR(3) = 2*(B013R+B024R)-8*(C123R(4,2)+C134R(4,2))+32*D1234R(22,
     -   4)+D01234R*P(1)+C0123R*P(3)+C0124R*P(4)+C0134R*P(5)+C0234R*P
     -   (6)+4*D1234R(7,2)*P(14)
       FI(3) = 2*(B013I+B024I)-8*(C123I(4,2)+C134I(4,2))+32*D1234I(22,
     -   4)+D01234I*P(1)+C0123I*P(3)+C0124I*P(4)+C0134I*P(5)+C0234I*P
     -   (6)+4*D1234I(7,2)*P(14)
       F(3)=DCMPLX(FR(3),FI(3))
       P(15) = p1sq-s12
       P(16) = p2sq+2*P(15)
       FR(4) = 4*(C0124R-C0134R+C124R(1,1)-3*C134R(1,1)-2*C134R(1,2)+4
     -   *D1234R(7,2)+12*D1234R(11,3)+8*D1234R(16,4)+D1234R(1,2)*P(10
     -   )+D01234R*P(15)+D1234R(1,1)*P(16))
       FI(4) = 4*(C0124I-C0134I+C124I(1,1)-3*C134I(1,1)-2*C134I(1,2)+4
     -   *D1234I(7,2)+12*D1234I(11,3)+8*D1234I(16,4)+D1234I(1,2)*P(10
     -   )+D01234I*P(15)+D1234I(1,1)*P(16))
       F(4)=DCMPLX(FR(4),FI(4))
       P(17) = p4sq-s12
       P(18) = p3sq+2*P(17)
       FR(5) = 4*(-C0123R+C0124R-3*C123R(1,1)-2*C123R(1,2)+C124R(1,1)+
     -   4*D1234R(7,2)+12*D1234R(11,3)+8*D1234R(16,4)+D1234R(1,2)*P(1
     -   2)+D01234R*P(17)+D1234R(1,1)*P(18))
       FI(5) = 4*(-C0123I+C0124I-3*C123I(1,1)-2*C123I(1,2)+C124I(1,1)+
     -   4*D1234I(7,2)+12*D1234I(11,3)+8*D1234I(16,4)+D1234I(1,2)*P(1
     -   2)+D01234I*P(17)+D1234I(1,1)*P(18))
       F(5)=DCMPLX(FR(5),FI(5))
       FR(6) = -8*(C124R(1,1)+C124R(1,2)-4*(D1234R(11,3)+D1234R(16,4))
     -   )+4*(D1234R(1,1)+D1234R(1,2))*P(13)
       FI(6) = -8*(C124I(1,1)+C124I(1,2)-4*(D1234I(11,3)+D1234I(16,4))
     -   )+4*(D1234I(1,1)+D1234I(1,2))*P(13)
       F(6)=DCMPLX(FR(6),FI(6))
       P(19) = p1sq+p4sq
       P(20) = s23-2*P(19)
       FR(7) = -4*(C0123R+C0134R+C123R(1,1)+C134R(1,1)-16*D1234R(11,3)
     -   -8*(D1234R(7,2)+D1234R(16,4))-D1234R(1,2)*P(11)-D01234R*P(19
     -   )+D1234R(1,1)*P(20))
       FI(7) = -4*(C0123I+C0134I+C123I(1,1)+C134I(1,1)-16*D1234I(11,3)
     -   -8*(D1234I(7,2)+D1234I(16,4))-D1234I(1,2)*P(11)-D01234I*P(19
     -   )+D1234I(1,1)*P(20))
       F(7)=DCMPLX(FR(7),FI(7))
       P(21) = p3sq-s23
       P(22) = 2*P(15)+P(21)
       P(23) = p1sq+p3sq-s12-s23
       FR(8) = -4*(C0124R-C0134R+C124R(1,1)-C134R(1,1)-4*D1234R(7,2)-1
