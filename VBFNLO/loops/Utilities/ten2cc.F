c Contraction of T^munu T^munu
       complex*16 T1(0:3,0:3),T2(0:3,0:3)
       complex*16 ten2cc
c
      ten2cc=T1(0,0)*T2(0,0) - T1(0,1)*T2(0,1) - T1(0,2)*T2(0,2) - T1(0,3)*T2(0,3) - 
     -T1(1,0)*T2(1,0) + T1(1,1)*T2(1,1) + T1(1,2)*T2(1,2) + T1(1,3)*T2(1,3) - 
